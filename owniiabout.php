            <div>
                <p>Waste To Energy (WTE) is the general description of technologies that are deployed to conver waste to useful energy. Different types of waste can be used as raw materials for the energy conversion process, ranging from organic, inorganic, industrail, biomass, municipal, food, etc. Useful energy so generated from the conversion process primarily includes the likes of synthiesis gas and heat. These main products are utually further utlized or converted into other forms of utilziation such as heating or electricity.</p>
                <br>
                <p>In the specific context of WTE for the Company, we are focusing on the deployment of our WTE Technologies across different spectrum of waste available, and converting them into heat and electricity.</p>
                <p>As one of the several WTE Technologies, the C6T Advanced Recycling and Energy Conversion Process (“ARECP”) incorporates several innovative technologies, four of which have patents. The operating principle of this technology is to process the waste to remove recyclables, non-convertible inert gas/materials and excess water, and then generate an intermediate gas or liquid fuel from the incoming waste by means of pyrolysis. This intermediate fuel is cleaned and then used in engines to produce electricity and also the syngas can be utilized for industrial or residential usage.</p>
                <br>
                <p>Direct applications are currently predominantly in the rehabilitation of landfills, as well as utilization of waste as feedstock for the generation of electricity.</p>
                <br>
                <p>Brief overview of Advanced Thermal Conversion Methods - Advanced thermal conversion techniques used to generate renewable energy from waste, should not be confused with traditional mass-burn incineration or gasification. Pyrolysis is dissimilar to burning, which requires oxygen. The WTE pyrolysis technology employed here is using an advanced thermal conversion technique which is a "clean, reliable, renewable source of energy," according to the US Environmental Protection Agency.</p>
                <p>The key feature underpinning the C6T ARECP is the pyrolization process, that is the chemical process of burning and breaking down materials in the absence of oxygen.</p>
                <br>
                <p>All the waste inclusive of organic compounds will, if heated in a controlled manner, break down into lower molecular weight compounds, changing physical form as their molecular weight decreases. Within this reaction, organic solids will be converted first into liquids and then into gases.</p>
                <br>
                <p>On the basis that heating is achieved in an oxygen-starved environment, the thermo-chemical decomposition process is termed gasification. Pyrolysis is an advanced form of gasification and takes place at elevated temperatures in the absence of oxygen. Pyrolysis can treat many different solid hydrocarbon-based wastes. Pyrolysis and gasification are dissimilar to burning, which requires oxygen.</p>
                <br>
                <p>Pyrolysis can be used to produce syngas or liquid products. Pyrolysis can produce a clean fuel gas mixture of methane, carbon monoxide and hydrogen called “syngas”. This syngas will typically have a calorific value of 19 - 30 MJ/m3 depending on the waste material being processed. The lower calorific value is associated with biomass waste, the higher calorific value being associated with other wastes such as sewage sludge and hydrocarbon base waste. Syngas can be produced with higher calorific values when the waste contains quantities of materials with a high calorific value, such as rubber.</p>
                <br>
                <p>Pyrolysis can also be used to produce liquid fuel, like diesel, which can be readily stored and transported.</p>
                <br>
                <p>Syngas is suitable for utilization in either gas engine to generate electricity, or in boiler applications. Syngas burns more efficiently and cleanly than the waste from which it was made.</p>
                <br>
                <p>Biomass pyrolysis can thus improve the efficiency of large-scale biomass power facilities such as those for forest industry residues and specialized facilities such as black liquor recovery boilers of the pulp and paper industry, both major sources of biomass power. Like natural gas, syngas can also be burned in gas turbines, a more efficient electrical generation technology than steam boilers to which solid biomass and fossil fuels are limited. The pyrolysis process has successfully operated gas engines utilizing many different biomasses as the feed source.</p>
                <br>
                <p>During the gas cooling phase, a gas clean-up system is employed to render the syngas “engine friendly”. Pyrolysis leaves a solid inert residue of between 5 to 7% of the processed waste.</p>
